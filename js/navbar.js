function setNavbarSmallScreen() {
    if (window.innerWidth < 770) {
        setCorrectLanguage("navbarTemplateId", navbarTemplateSerbian, navbarTemplateEnglish, navbarTemplateGerman);
    } else {
        setCorrectLanguage("navbarTemplateId", navbarTemplateSmallWindowSerbian, navbarTemplateSmallWindowEnglish, navbarTemplateSmallWindowGerman);
    }
    setSelectedPage()
}

function setSelectedPage() {
    if (currentLocation) {
        document.getElementById(currentLocation).style.color = "rgb(255, 123, 123)";
        if (currentLocation == "homeTab") {
            setCorrectLanguage("pageHeaderTitle", homeTitleSerbian, homeTitleEnglish, homeTitleGerman);
            setCorrectLanguageContent("pageMetaDesc", homeMetaDescSerbian, homeMetaDescEnglish, homeMetaDescGerman);
            setCorrectLanguageContent("pageMetaKeywords", homeMetaKeywordsSerbian, homeMetaKeywordsEnglish, homeMetaKeywordsGerman);

        } else if (currentLocation == "aboutTab") {
            setCorrectLanguage("pageHeaderTitle", aboutTitleSerbian, aboutTitleEnglish, aboutTitleGerman);
            setCorrectLanguageContent("pageMetaDesc", aboutMetaDescSerbian, aboutMetaDescEnglish, aboutMetaDescGerman);
            setCorrectLanguageContent("pageMetaKeywords", homeMetaKeywordsSerbian, homeMetaKeywordsEnglish, homeMetaKeywordsGerman);

        } else if (currentLocation == "galleryTab") {
            setCorrectLanguage("pageHeaderTitle", galleryTitleSerbian, galleryTitleEnglish, galleryTitleGerman);
            setCorrectLanguageContent("pageMetaDesc", galleryMetaDescSerbian, galleryMetaDescEnglish, galleryMetaDescGerman);
            setCorrectLanguageContent("pageMetaKeywords", homeMetaKeywordsSerbian, homeMetaKeywordsEnglish, homeMetaKeywordsGerman);

        } else if (currentLocation == "contactTab") {
            setCorrectLanguage("pageHeaderTitle", contactTitleSerbian, contactTitleEnglish, contactTitleGerman);
            setCorrectLanguageContent("pageMetaDesc", contactMetaDescSerbian, contactMetaDescEnglish, contactMetaDescGerman);
            setCorrectLanguageContent("pageMetaKeywords", homeMetaKeywordsSerbian, homeMetaKeywordsEnglish, homeMetaKeywordsGerman);

        }
    }
}